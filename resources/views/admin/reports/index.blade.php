    document.addEventListener('DOMContentLoaded', function() {
        // Set width for user trend bars
        var userTrendBars = document.querySelectorAll('.user-trend-bar');
        userTrendBars.forEach(function(bar) {
            var count = parseInt(bar.getAttribute('data-count'));
            var max = parseInt(bar.getAttribute('data-max'));
            var width = count > 0 ? (count / max * 100) : 0;
            bar.style.width = width + '%';
        });
        
        // Set width for event trend bars
        var eventTrendBars = document.querySelectorAll('.event-trend-bar');
        eventTrendBars.forEach(function(bar) {
            var count = parseInt(bar.getAttribute('data-count'));
